#include "libft.h"

int		ft_atoi(char *s)
{
	long	prv;
	long	n;
	int		sign;

	n = 0;
	while ((*s >= 9 && *s <= 13) || (*s == 32))
		s++;
	sign = 1;
	if (*s == '-')
		sign = -1;
	if (*s == '+' || *s == '-')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		prv = n;
		n = n * 10 + (*s++ - '0');
		if (prv > n)
		{
			if (sign == 1)
				return (-1);
			return (0);
		}
	}
	return ((int)(n * sign));
}
